package gov.idaho.isp.fitness.challenge.service;

public class EmployeeRankStats {
  private Integer situpPushupRank;
  private Integer resistanceRank;
  private Integer aerobicHoursRank;
  private Integer runRank;
  private Integer swimRank;
  private Integer bikeRank;

  public Integer getSitupPushupRank() {
    return situpPushupRank;
  }

  public void setSitupPushupRank(Integer situpPushupRank) {
    this.situpPushupRank = situpPushupRank;
  }

  public Integer getResistanceRank() {
    return resistanceRank;
  }

  public void setResistanceRank(Integer resistanceRank) {
    this.resistanceRank = resistanceRank;
  }

  public Integer getAerobicHoursRank() {
    return aerobicHoursRank;
  }

  public void setAerobicHoursRank(Integer aerobicHoursRank) {
    this.aerobicHoursRank = aerobicHoursRank;
  }

  public Integer getRunRank() {
    return runRank;
  }

  public void setRunRank(Integer runRank) {
    this.runRank = runRank;
  }

  public Integer getSwimRank() {
    return swimRank;
  }

  public void setSwimRank(Integer swimRank) {
    this.swimRank = swimRank;
  }

  public Integer getBikeRank() {
    return bikeRank;
  }

  public void setBikeRank(Integer bikeRank) {
    this.bikeRank = bikeRank;
  }
}